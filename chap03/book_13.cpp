//Detects adjacent repeated words in sequence of words

#include<iostream>
#include<string>

using namespace std;

int main(){
  double number = 0;
  cout << "Insert a number: ";
  while(cin >> number){
    int double2int = number;
    char int2char = double2int;
    int char2int = int2char;
    cout << "double: "<<number << '\n';
    cout << "to integer: "<<double2int << '\n';
    cout << "to char: "<<int2char << '\n';
    cout << "to int: "<<char2int << '\n';
    cout << "\nInsert a number: ";
    }
}
